let nome = prompt("Digite o seu nome.")
alert(`Olá, ${nome}!`)
let primeiroNumero = parseInt(prompt(`${nome}, digite um número.`))

const numCinco = 5
let soma = primeiroNumero + numCinco
let subtracao = primeiroNumero - numCinco
let multiplicacao = primeiroNumero * numCinco
let divisao = primeiroNumero / numCinco

alert(`Resultados do número que você digitou calculados com o número 5: Soma = ${soma} || Subtração = ${subtracao} || Multiplicação = ${multiplicacao} || Divisão = ${divisao}`)
console.log(`Olá, ${nome} os resultados são: Soma = ${soma} || Subtração = ${subtracao} || Multiplicação = ${multiplicacao} || Divisão = ${divisao}`)
